typedef enum {
	LED_OFF = 0,
	LED_ON,
	LED_SWITCH
} LED_STATUS;

void _led_control_internal(LED_STATUS status, uint16_t led1, ...) {
	static bool flags[4] = {false};
	static uint16_t leds[4] = {GPIO_PIN_12, GPIO_PIN_13, GPIO_PIN_14, GPIO_PIN_15};

	va_list args;
	va_start(args, led1);

	uint16_t current = led1;
	while (current != 0xFFFF) {
		GPIO_PinState state;
		if (status == LED_SWITCH) {
			for (int i = 0; i < 4; ++i) {
				if (leds[i] == current) {
					state = !flags[i];
					flags[i] = !flags[i];
					break;
				}
			}
		}
		else {
			state = status;
		}

		HAL_GPIO_WritePin(GPIOD, current, state);
		current = (uint16_t) va_arg(args, int);
	}

	va_end(args);
}

#define switch_led(...) _led_control_internal(LED_SWITCH, __VA_ARGS__, 0xFFFF);
#define enable_led(status, ...) _led_control_internal(status,  __VA_ARGS__, 0xFFFF);

